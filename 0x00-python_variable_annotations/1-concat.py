#!/usr/bin/env python3
""" this is a typed annotated function concat that takes to strings and returns them"""


def concat(str1: str, str2: str) -> str:
    """
    the function concat take 2 string  to be concatenate
    
    Args:
        str1 string value 
        str2 string value 

    Returns:
        this returns the concatenated strings
    """
    return str1 + str2
